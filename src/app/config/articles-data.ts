/**
 * 文章数据源 - 元感知
 *
 * 所有文章内容集中管理，按 card id 索引
 * 承接首页「今日频率指南」和「明镜之声」卡片的点击跳转
 *
 * 数据规范：
 *   - 标题不硬编码省略号，截断由 CSS line-clamp 处理
 *   - 副标题分隔符用中圆点（·）
 *   - 文章 800-1500 字，陪伴式提问调性
 *   - 每篇文末附微练习
 *   - content 段落支持多种类型，由阅读页模板渲染
 *   - 中文引号统一用角引号「」，避免 ASCII 引号冲突
 *
 * 段落类型：
 *   text     — 正文
 *   heading  — 小标题（宋体）
 *   quote    — 引述/金句（琥珀竖线）
 *   question — 陪伴式提问（鼠尾草绿底）
 *   divider  — 装饰分隔线
 */

// ─── 类型定义 ─────────────────────────────────────────

export interface ArticleParagraph {
  type: "text" | "heading" | "quote" | "question" | "divider";
  content: string;
}

export interface MicroPractice {
  /** 练习标题 */
  title: string;
  /** 练习步骤 */
  steps: string[];
  /** 预估时长 */
  duration: string;
}

export interface Article {
  /** 唯一标识，对应 home-data 中的 card id */
  id: string;
  /** 文章标题 */
  title: string;
  /** 副标题/来源 */
  subtitle: string;
  /** 所属栏目 */
  category: string;
  /** 栏目色调 amber | sage */
  categoryColor: "amber" | "sage";
  /** 封面图 */
  coverImage: string;
  /** 作者 */
  author: {
    name: string;
  };
  /** 阅读时长 */
  readTime: string;
  /** 可选音频路径 */
  audioSrc?: string;
  /** 正文段落 */
  paragraphs: ArticleParagraph[];
  /** 文末微练习 */
  practice?: MicroPractice;
}

